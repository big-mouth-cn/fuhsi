package org.bigmouth.fuhsi.servicelogic.factory;

public class PlugInDiscoverException extends RuntimeException {

	private static final long serialVersionUID = 4882309242255444093L;

	public PlugInDiscoverException() {
		super();
	}

	public PlugInDiscoverException(String message, Throwable cause) {
		super(message, cause);
	}

	public PlugInDiscoverException(String message) {
		super(message);
	}

	public PlugInDiscoverException(Throwable cause) {
		super(cause);
	}
}
