package org.bigmouth.fuhsi.servicelogic.factory;

public class TooManyPlugInAnnotationException extends PlugInDiscoverException {

	private static final long serialVersionUID = 5444972565172254370L;

	public TooManyPlugInAnnotationException() {
		super();
	}

	public TooManyPlugInAnnotationException(String message, Throwable cause) {
		super(message, cause);
	}

	public TooManyPlugInAnnotationException(String message) {
		super(message);
	}

	public TooManyPlugInAnnotationException(Throwable cause) {
		super(cause);
	}
}
